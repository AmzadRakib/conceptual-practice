function positiveOrNegative(num) {
    if (num == 0) {
        console.log('This Number Is Zero,');
    }
    else if (num > 0) {
        console.log('This is Positive Number');
    }
    else {
        console.log('This Is Negative Number,');
    }
}

positiveOrNegative(-2);
positiveOrNegative(0);
positiveOrNegative(2);